// dart supports static type and dynamic type variables
main() {
  String name1 = "John";
  var name2 = "Little John";

  print("Name1: $name1, Name2: $name2");

  int value1 = 100;
  var value2 = 101;

  print("Value1: $value1, Value2: $value2");

  double concession1 = 3.445;
  var concession2 = 44.21;

  print("Concession1: $concession1, Concession2: $concession2");

  bool state1 = true;
  var state2 = false;

  print("State1: $state1, State2: $state2");
}
